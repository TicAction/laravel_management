     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $school = new School([
            'school_name'=>$request->get('school_name'),
            'school_region'=>$request->get('school_region'),
            'school_adress'=>$request->get('$school_adress'),
            'school_city'=>$request->get('school_city'),
            'school_phone'=>$request->get('school_phone'),
            'school_email'=>$request->get('school_email'),
            'school_url'=>$request->get('school_url'),
        ]);
        $school->save();

        return redirect('ecole')->with('success','Enregistrement fait');

    }

